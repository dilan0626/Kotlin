package `1_Tipos_de_variables`

fun main() {
    //Todas las variables val son inmutables
// Variable inmutable de tipo entero
    val edad: Int
    edad = 48
    //Variable inmutable de tipo decimal
    val sueldo: Float
    sueldo = 1200.55f // siempre lleva una "f" al final del valor
    //Variable inmutable de tipo decimal
    val total: Double
    total = 70000.24
    // Variable inmutable de tipo alfanumerico
    val titulo: String
    titulo = "Sistema de ventas"
    // Variable mutable, las variables mutables empiezan con "var"
    var mes: Int
    mes = 1
    //algunas lineas mas de codigo
    mes = 2
    // Variable de tipo Char, almacna un unico caracter
    var continuar: Char = 's'
    // Variable de tipo Booleano, true, false
    var fin: Boolean = false

// lo crea seguido
    print("Dilan")
    print("jesus")

    //crea un salto de linea
    println("Carranza")
    println("Enriquez")

}